package com.forLoop;

public class Pattern05 {
	public static void pattern()
	{
		for(int i=6;i>=1;i--)
		{
			for(int j=1;j<=5;j++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
	}
		
		public static void main(String[] args) {
			
			
	      pattern();
		}


}
