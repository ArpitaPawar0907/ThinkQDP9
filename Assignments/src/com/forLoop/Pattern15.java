package com.forLoop;

public class Pattern15 {
	public static void pattern()
	{
		for(int i='A';i<='I';i++)
		{
			
			for(int j='A';j<=i;j++)
			{
				System.out.print((char)j);
				
			}
			System.out.println();
		}
	}
		
		public static void main(String[] args) {
			
			
	      pattern();
		}



}
